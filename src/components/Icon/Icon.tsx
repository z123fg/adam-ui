export enum IconSize {
    Large = 'lg',
    Medium = 'md',
    Small ='sm',
}

export enum IconType {
    Default = 'default',
    Danger = 'danger',
    Safe = 'safe',
}

export enum IconColor {
    Red = 'red',
    Gray = 'gray',
    Black = 'black',
    Blue = 'blue',
}

export interface IMyIconProps {
    iconSize?: IconSize; //diff sizes: lg, md, sm
    iconType?: IconType; // default, danger
    iconColor?: IconColor; // diff icon colors
    className?: string;
    name?: string;
    disabled?: boolean;
}

const Icon: React.FunctionComponent<IMyIconProps> = (props)=> {

    const {
        iconSize = IconSize,
        iconType = IconType, 
        iconColor = IconColor,
        className = ' ',
        name,
        disabled = false,
        ...rest
    } = props

    let styleClasses = classNames('icon', {
        [`icon-${iconType}`]: !!iconType,
        [`icon-${iconSize}`]: !!iconSize,
        [`icon-${iconColor}`]: !!iconColor,
        [`${name}`]: true,
        disabled: !!disabled,
    });

    if(disabled) {
        styleClasses += ' ' + 'disabled';
        return <svg className={styleClasses} >
            <path/>
        </svg>
    } else {
        if (className) {
            styleClasses += ' ' + className;
        } 
        return <svg className={styleClasses} >
            <path />
        </svg>
    }
}

export default Icon;